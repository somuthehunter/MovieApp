/// A utility class containing error codes used throughout the application.
///
/// This class provides a centralized location for defining and managing error codes.
class ErrorCodes {
  /// Error code for unknown exception.
  ///
  /// This code is used to identify unknown or unspecified exceptions.
  static String unknownException = 'UKN00';

  /// Error code for no internet connectivity.
  ///
  /// This code is used to indicate that the device has no internet connection.
  static String noInternetException = 'NOINT00';
}
